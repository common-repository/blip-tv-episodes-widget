<h2><strong>WebTechGlobal Development Notes</strong> For Blip TV Episodes Widget</h2>
<p><strong>Updated: 18th June 2010</strong></p>
<h2><strong> Improvements Coming</strong></h2>
<p>The main focus for this plugin will be to make use of Blip TV's episode niche and provide tools that help bundle videos together. I also want the plugin to be a management tool&nbsp;so that once your videos are uploaded to Blip TV and you have your url for them, you can drive traffic to your own blog instead of Blip TV. By making your viewers aware that all content is provided on your own site, it will increase your sites traffic&nbsp;and hopefully increase revenue from things like AdSense or expose other content along&nbsp;with the videos.</p>
<ol>
  <li>Ability to add a paging system to posts for specific episodes.</li>
  <li>Newsletter system with each&nbsp;video collection having its own email list</li>
  <li>WYSIWYG editor for designing newsletters and automated emails.</li>
</ol>
